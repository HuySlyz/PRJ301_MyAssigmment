package dto;

import java.util.Date;

/**
 *
 * @author Huy
 */
public class ObjDetailDTO extends ObjDTO{
    
    String usercreate;

    public ObjDetailDTO(String usercreate, int id, String name, boolean gender, Date dob, String groupname) {
        super(id, name, gender, dob, groupname);
        this.usercreate = usercreate;
    }

    public ObjDetailDTO() {
    }

    public String getUsercreate() {
        return usercreate;
    }

    public void setUsercreate(String usercreate) {
        this.usercreate = usercreate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public String getGroupname() {
        return groupname;
    }

    public void setGroupname(String groupname) {
        this.groupname = groupname;
    }
    
    
}
