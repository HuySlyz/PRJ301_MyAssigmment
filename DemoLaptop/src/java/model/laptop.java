package model;

/**
 *
 * @author Huy
 */
public class laptop {
    int id;
    String name;
    int price;
    String color;

    public laptop() {
    }

    public laptop(int id, String name, int price, String color) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.color = color;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    @Override
    public String toString() {
        return "laptop{" + "id=" + id + ", name=" + name + ", price=" + price + ", color=" + color + '}';
    }
    
}
